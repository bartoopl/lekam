                        </p>
                        <ul>
                            <li>Zapewnienia funkcjonalności platformy</li>
                            <li>Zapamiętywania preferencji użytkownika</li>
                            <li>Analizy ruchu na platformie</li>
                            <li>Personalizacji treści</li>
                        </ul>

                        <h2>10. Analityka</h2>
                        <p>
                            Używamy narzędzi analitycznych do monitorowania:
                        </p>
                        <ul>
                            <li>Liczby odwiedzin platformy</li>
                            <li>Popularności kursów</li>
                            <li>Skuteczności testów</li>
                            <li>Zachowań użytkowników (anonimowo)</li>
                        </ul>

                        <h2>11. Międzynarodowe przekazywanie danych</h2>
                        <p>
                            Dane mogą być przekazywane poza EOG tylko do państw 
                            zapewniających odpowiedni poziom ochrony danych osobowych 
                            lub na podstawie odpowiednich zabezpieczeń.
                        </p>

