                        <p>Przetwarzamy następujące kategorie danych osobowych:</p>
                        <ul>
                            <li>Dane identyfikacyjne (imię, nazwisko)</li>
                            <li>Dane kontaktowe (adres e-mail)</li>
                            <li>Dane zawodowe (typ użytkownika, numer licencji)</li>
                            <li>Dane o postępach w nauce i wynikach testów</li>
                            <li>Dane techniczne (adres IP, informacje o urządzeniu)</li>
                        </ul>

                        <h2>3. Cel przetwarzania danych</h2>
                        <p>Dane osobowe są przetwarzane w następujących celach:</p>
                        <ul>
                            <li>Świadczenie usług e-learningowych</li>
                            <li>Zarządzanie kontem użytkownika</li>
                            <li>Śledzenie postępów w nauce</li>
                            <li>Generowanie certyfikatów</li>
                            <li>Komunikacja z użytkownikami</li>
                            <li>Analiza i poprawa jakości usług</li>
                        </ul>

                        <h2>4. Podstawa prawna przetwarzania</h2>
                        <p>
                            Przetwarzanie danych odbywa się na podstawie:
                        </p>
                        <ul>
                            <li>Art. 6 ust. 1 lit. b) RODO - wykonanie umowy</li>
                            <li>Art. 6 ust. 1 lit. f) RODO - prawnie uzasadniony interes</li>
                            <li>Art. 6 ust. 1 lit. a) RODO - zgoda (w określonych przypadkach)</li>
                        </ul>

                        <h2>5. Okres przechowywania danych</h2>
                        <p>
                            Dane osobowe przechowujemy przez okres:
                        </p>
                        <ul>
                            <li>Dane konta użytkownika - przez czas trwania konta</li>
                            <li>Dane o postępach w nauce - 5 lat od ostatniej aktywności</li>
                            <li>Certyfikaty - 10 lat od wydania</li>
                            <li>Dane techniczne - 2 lata</li>
                        </ul>

                        <h2>6. Prawa użytkowników</h2>
                        <p>Użytkownicy mają prawo do:</p>
                        <ul>
                            <li>Dostępu do swoich danych osobowych</li>
                            <li>Sprostowania nieprawidłowych danych</li>
                            <li>Usunięcia danych (prawo do bycia zapomnianym)</li>
                            <li>Ograniczenia przetwarzania</li>
                            <li>Przenoszenia danych</li>
                            <li>Wniesienia sprzeciwu</li>
                            <li>Cofnięcia zgody (jeśli była udzielona)</li>
                        </ul>

                        <h2>7. Bezpieczeństwo danych</h2>
                        <p>
                            Stosujemy odpowiednie środki techniczne i organizacyjne 
                            w celu ochrony danych osobowych przed:
                        </p>
                        <ul>
                            <li>Nieuprawnionym dostępem</li>
                            <li>Utratą danych</li>
                            <li>Zniszczeniem danych</li>
                            <li>Zmianą danych</li>
                        </ul>

                        <h2>8. Udostępnianie danych</h2>
                        <p>
                            Dane osobowe mogą być udostępniane:
                        </p>
                        <ul>
                            <li>Dostawcom usług technicznych (hosting, analityka)</li>
                            <li>Organom administracji publicznej (na żądanie)</li>
                            <li>Innym użytkownikom (tylko w zakresie certyfikatów)</li>
                        </ul>

